### 5. Study the script
The discussion script is a support tool that seeks to guide the conversation during the focus group according to the research objectives. It contains open-ended questions and topics designed to encourage reflection and discussion. You should study the script several times before the activity so that you will be able to explore new topics without losing sight of the objectives of the session. During this process of studying the script, identify:

::: {.callout-note}
## The role of the facilitator
In addition to understanding the script, the facilitator must also understand the research questions of the project. This will allow them to probe beyond the guide and see, during the execution of the activity, possible questions or dimensions of the problem that were not known when the script was developed. 
:::

## Conducting a focus group
To start the focus group, it is important that you introduce yourself and clearly explain the role of each member of the work team, and the overall dynamics of the session. Next, read the informed consent, ensuring all participants clearly understand the purpose of the activity and the potential risks and benefits of their participation. During this process, emphasize the confidentiality and anonymity of the information collected during the focus group. The moderator should explicitly obtain the participants' authorization to record the session. Throughout the activity, create a comfortable and calm environment that allows participants to express their opinions spontaneously and without censorship. To maintain a favorable atmosphere, you should show interest, cordiality and respect for all interventions , having an attitude of curiosity, even when the topics are extended or the opinions expressed differ from yours. 

