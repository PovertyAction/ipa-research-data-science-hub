## What is a focus group?

Focus groups are one of the most widely used techniques in social research, particularly within qualitative studies. This method involves creating a discussion space where three to eight participants engage in an in-depth conversation about one or more topics.[^1]

[^1]: Jenny Kitzinger, "Qualitative Research: introducing focus groups," (1999).  BMJ 311:299-302.doi: 10.1136/bmj.311.7000.299. In a typical focus group, a **moderator** leads the discussion, ensuring active interaction among participants. The moderator's role is to ask questions that stimulate discussion, guide the conversation to gather valuable insights, and build trust with the participants. This trust is crucial to making participants feel comfortable expressing themselves freely, ensuring the discussion remains productive and focused on relevant topics.

![Figure 1. An example of a focus group setting](/assets/images/focus-group-example.jpg){fig-cap="This is the title of  figure"}

Focus groups are artificially configured environments, characterized by collecting information related to the interaction among participants to explore their perceptions, opinions, and attitudes toward specific topics. This technique allows one to collect data on how participants communicate, share their opinions and experiences, and react to the contributions of others. 

::: {.callout-tip}
## Example: Perceptions from Venezuelan Migrants

In a focus group with Venezuelan migrants in Colombia, several common perceptions about the healthcare system emerged. While some participants acknowledged the accessibility of essential medical services as an advantage of the Colombian system, others expressed concerns about the quality of care, long wait times, and challenges in accessing specialized services, particularly due to their migratory status.
:::

